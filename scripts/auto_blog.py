def generate_and_post():
    print("🤖 Генерирую статью для блога 'Дико-Корисно'...")
    
    try:
        response = client.chat.completions.create(
            model="gpt-4o",
            messages=[
                {
                    "role": "system",
                    "content": """Ти — професійний копірайтер і експерт з біохакінгу магазину DikorosUA.
Пиши статті в стилі нативного лонгріду: глибоке занурення в тему, наукові факти, жирні акценти на користі та чітка структура (заголовки H3, списки).
Мова: строго українська (основний контент сайту).
У кожній статті обов'язково органічно вплетай 3–5 товарів із наданої бази — посилання у форматі [Назва товару](dikorosua://product/ID). Не роби перелік посилань наприкінці: посилання мають бути всередині тексту в контексті речень."""
                },
                {
                    "role": "user",
                    "content": f"""Напиши захоплюючу статтю-лонгрід для блогу «Дико-Корисно» (тема: мікродозинг, гриби, трави або здоров'я — на твій вибір).

БАЗА ТОВАРІВ ДЛЯ ПОСИЛАНЬ (використовуй тільки ці ID):
{BLOG_PRODUCTS_BASE}

ПРАВИЛА:
1. Мова: строго українська.
2. Стиль: нативний лонгрід — заголовки ### (H3), списки, **жирні** акценти на користі та активних речовинах, наукові факти.
3. Товари: обов'язково 3–5 посилань у форматі [Назва](dikorosua://product/ID). Вставляй їх органічно в речення (наприклад: «Для покращення сну рекомендуємо спробувати [Мікродозінг Brain & Sleep Їжовик гребінчастий](dikorosua://product/39186), який м'яко заспокоює нервову систему»). Не роби окремий список посилань в кінці.
4. Завершення: фірмовий підпис на кшталт «Залишайтеся здоровими разом із Дико-Корисно!».

Поверни СУВОРО JSON: {{"title": "...", "content": "..."}}"""
                }
            ],
            response_format={"type": "json_object"}
        )
        
        post_data = json.loads(response.choices[0].message.content)

        # Генерация картинки
        print(f"🎨 Рисую обложку для темы: {post_data['title']}...")
        img_response = client.images.generate(
            model="dall-e-3",
            prompt=f"Aesthetic high-end photography for 'Diko-Korisno' blog. Theme: mushrooms, wellness, organic lifestyle. Professional lighting.",
            size="1024x1024",
            n=1,
        )
        # Скачиваем картинку с DALL-E и сохраняем локально (временные URL быстро умирают)
        image_url_from_dalle = img_response.data[0].url
        uploads_dir = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", "uploads"))
        os.makedirs(uploads_dir, exist_ok=True)
        ext = "png"  # DALL-E 3 возвращает PNG
        local_filename = f"image_{uuid.uuid4().hex}.{ext}"
        local_path = os.path.join(uploads_dir, local_filename)
        try:
            r = requests.get(image_url_from_dalle, timeout=30)
            r.raise_for_status()
            with open(local_path, "wb") as f:
                f.write(r.content)
            post_data["image_url"] = f"/uploads/{local_filename}"
            print(f"📁 Изображение сохранено: {post_data['image_url']}")
        except Exception as e:
            print(f"⚠️ Не удалось скачать изображение: {e}. Сохраняем пост без обложки.")
            post_data["image_url"] = None

        # Публикация
        res = requests.post(API_URL, json=post_data)
        if res.status_code == 200:
            print("✅ ПОБЕДА: Статья для 'Дико-Корисно' успешно опубликована!")
        else:
            print(f"❌ Ошибка: {res.text}")

    except Exception as e:
        print(f"💥 Ошибка: {str(e)}")

if __name__ == "__main__":
    generate_and_post()